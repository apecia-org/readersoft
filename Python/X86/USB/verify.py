def calculate_checksum(uBuff):
    uSum = 0
    for byte in uBuff:
        uSum += byte
    uSum = (~uSum) + 1
    return uSum & 0xFF  # Ensure the result is an 8-bit unsigned integer

# Example usage
uBuff = [0x43, 0x54, 0x00, 0x1C, 0x01, 0x45, 0x01, 0xC3, 0x8B, 0x22, 0x11, 0x02, 0x2E, 0xF3, 0x01, 0x0F, 0x01, 0x01, 0xE2, 0x00, 0x47, 0x07, 0xA7, 0x50, 0x68, 0x21, 0xDF, 0x3C, 0x01, 0x0C, 0xC8]

checksum = calculate_checksum(uBuff)
print(f"Checksum: {hex(checksum)}")
